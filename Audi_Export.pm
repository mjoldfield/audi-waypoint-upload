=head1 NAME

Audi_Export - Proof of concept waypoint exporter for Audi MMI

=head1 SYNOPSIS

    use Audi_Export;
    
    my $ex = My::Audi_Export->new('ex-test');
    
    $ex->define_type('good', 'Goodies', "audi-icons/found.png");
    $ex->define_type('bad',  'Baddies', "audi-icons/cache.png");
    
    $ex->add_location(52.206944, 0.116944, "Trinity",   'good');
    $ex->add_location(52.206944, 0.130944, "St Johns",  'bad');
    $ex->add_location(52.209264, 0.092011, "Cavendish", 'good');
    
    $ex->save;
   
=head1 DESCRIPTION

Uploading waypoints to the Audi MMI (2014 A3 version) can be done
via the myAudi website, but it is painful and involves Java! This
is an attempt to do the whole thing locally.

=head1 METHODS

=over

=item new($stem)

Constructor. The files will be created under C<$stem> .

=item define_type($tag, $name, $icon_path)

Define a new type of waypoints. C<$tag> is an opaque tag used purely
by this API; C<$name> gets exported and appears in the car.

C<$icon_path> must point to a file containing an icon. I have
only tested it with 33x33 pixel PNG files: the size at least
is implicitly embedded in other places.

=item add_location($n, $e, $name, $tag)

Add a waypoint with coordinates N C<$n> E C<$e>, called C<$name>,
and of type C<$tag>.

=item save

Write the files

=back

