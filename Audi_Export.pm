sub create_db
  {
    my $s = shift;

    my $path = 'PersonalPOI/Package/0/default/poidata.db';
    
    my $file = $s->filename($path);

    $s->mark_file($path);

    $s->make_path_for($file);
    unlink($file);
    
    my $db = DBI->connect('dbi:SQLite:dbname=' . $file, '', '');
    $db->{AutoCommit} = 1;
    
    $db->do(qq{CREATE VIRTUAL TABLE poicoord USING rtree(poiid INTEGER, latmin REAL, latmax REAL, lonmin REAL, lonmax REAL)});

    $db->do(qq{CREATE TABLE poidata(poiid INTEGER,type INTEGER,namephon TEXT,ccode INTEGER,zipcode TEXT,city TEXT,street TEXT,housenr TEXT,phone TEXT,ntlimportance INTEGER,exttype TEXT,extcont TEXT,warning TEXT,warnphon TEXT,CONSTRAINT PK_poidata PRIMARY KEY (poiid))});

    # Note the absence of poiid here: hardly normal form!
    # Instead insertion order matters!
    $db->do(qq{CREATE VIRTUAL TABLE poiname USING fts3 (name TEXT)});

    return $db;
  }

# Return sha1 checksums for data in a file, chunking it
# into blocks of size $bs.
sub blocked_sha256
  {
    my ($s, $file, $bs) = @_;

    my @cs;    
    open(my $fh, '<:raw', $file);

    my $buf;
    while(read($fh, $buf, $bs))
      {
	push(@cs, sha1_hex($buf));
      }

    return @cs;
  }

#
# Return the size of a file: if that file is hashes.txt
# then return the total size of the files to which reference
# is made instead.
#
sub size_of
  {
    my $f = shift;

    return -s $f unless $f =~ /hashes.txt$/;

    open(my $fh, '<', $f);

    my $sigma = -s $f;
    while(<$fh>)
      {
	$sigma += $1 if /FileSize\s*=\s*"(\d+)"/i;
      }

    return $sigma;
  }

