sub new
  {
    my ($class, $file) = @_;

    $file =~ s{/[^/]+$}{};

    return bless { dir     => $file, 
                   locs    => [],  # list of locations
		   type    => {},  # type -> [name, icon, index]
		   files   => [],  # list of files we've written
		   date    => sprintf("%04d%02d%02d", Today()),
		   subv    => sprintf("%02d%02d%02d", Now()),
		 }, $class;
  }
  
sub date       { return shift->{date}; }
sub subversion { return shift->{subv}; } 
  
sub version  { my $s = shift; return $s->date . '-' . $s->subversion }

# Given a path relative to the archive, return a path the FS will grok
sub filename
  {
    my ($s, $path) = @_;
    $path =~ s{^/}{};
    return $s->{dir} . '/'. $path;
  }

sub mark_file
  {
    my ($s, $path) = @_;
    push(@{$s->{files}}, $path);
  }

sub make_path_for
  {
    my ($s, $f) = @_;    
    (my $dir = $f) =~ s{/[^/]*$}{};
    make_path($dir);
  }
  
sub write_to_file
  {
    my ($s, $path, $text) = @_;

    $s->mark_file($path);
    
    my $f = $s->filename($path);

    $s->make_path_for($f);

    open(my $fh, '>:raw:bytes', $f);
    print {$fh} $text;
  }

sub copy_file
  {
    my ($s, $src, $dest) = @_;

    unless (-r $src)
      {
	warn "Unable to read $src: $!\n";
	return;
      }      
    
    my $contents = slurp('<:raw:bytes', $src);
    $s->write_to_file($dest, $contents || '');
  }
      
sub define_type
  {
    my ($s, $type, $name, $icon) = @_;

    croak "Type $type is already defined, "
      if $s->{type}->{$type};
    
    my $index = scalar keys %{$s->{type}};
    $s->{type}->{$type} = [ $name, $icon, $index ];
  }
    
sub add_location
  {
    my ($s, $lat, $lon, $name, $type) = @_;

    croak "Location $name ($lat,$lon) has type $type, which isn't defined, "
      unless $s->{type}->{$type};
    
    push(@{$s->{locs}}, [ $lon, $lat, $name, $type ]);
  }
  
sub save
  {
    my $s = shift;

    $s->write_static_files;
    
    my $db = $s->create_db;

    my $eps = 1e-7;
    
    my $i_wpt = 0;
    foreach my $w (@{$s->{locs}})
      {
	$i_wpt++;
        my ($lon, $lat, $name, $type) = @$w;
	my (undef, undef, $tindex) = @{$s->{type}->{$type}};

	$db->do(qq{INSERT INTO poiname(name) VALUES (?)}, undef, $name);
	$db->do(qq{INSERT INTO poicoord(poiid, latmin, latmax, lonmin, lonmax) 
                   VALUES (?,?,?,?,?)}, undef, 
		$i_wpt, $lat - $eps, $lat + $eps, $lon - $eps, $lon + $eps);
	$db->do(qq{INSERT INTO poidata(poiid, type) VALUES (?,?)}, undef,
		$i_wpt, $tindex);
      }
    
    $s->write_update_file;

    $s->write_to_file('PersonalPOI/Package/0/default/PPOIversion.txt',
		      sprintf("version.ota=%s\n", $s->version));

    $s->write_type_data;
        
    $s->write_hash_file;

    $s->write_metainfo_file;
  }

