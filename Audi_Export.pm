sub write_type_data
  {
    my $s = shift;

    my $bitmaps = '';  # for bitmaps.xml

    my $cats    = '';  # for categories.pc
    my $types   = '';
    my $srchs   = '';

    my $str_en  = ''; # for strings_en-GB.xml

    my $str_de  = ''; # for strings_de-DE.xml

    my @types = sort keys %{$s->{type}};

    foreach my $type (@types)
      {
	my ($tname, $icon, $tindex) = @{$s->{type}->{$type}};

	(my $suffix = $icon) =~ s/^.*\.//;

	my $index = sprintf "%d", 1010 + $tindex;
	
	my $name = 'image_' . $index . '.' . $suffix;

	# This is correct for a 33x33 icon
	my $icon_args = '0,0,33,33,-24,-24';

	my $full_name = "bitmaps/$name,$icon_args";
	
	$bitmaps .= qq{<resource id="$index" name="$full_name"/>};

	$cats    .= qq{<category bitmapIndex="$index" id="$tindex" name="$tindex" type="0">}
	         .  qq{<bitmap res_id="$index">$full_name</bitmap></category>};
	
	$types   .= qq{<type id="$tindex">}
	         .  qq{<bitmap module="0" res_id="$index" size="10">$full_name</bitmap>}
	         .  qq{<bitmap module="1" res_id="$index" size="10">$full_name</bitmap>}
	         .  qq{<bitmap module="1" res_id="$index" size="20">$full_name</bitmap>}
		 .  qq{<zoomlevel max="9" min="0"/><priority>1</priority><code>$tindex</code>}
		 .  qq{<description/>}
		 .  qq{</type>};

	$srchs   .= qq{<category id="$tindex" index="10"><type id="$tindex"/></category>};

	$str_en  .= qq{<string id="$tindex" type="0"><lang lang="en-GB"/><text>$tname</text></string>};
	$str_de  .= qq{<string id="$tindex" type="0"><lang lang="de-DE"/><text>$tname</text></string>};

	$s->copy_file($icon, 'PersonalPOI/Package/0/default/bitmaps/' . $name);
      }

    my $bitmaps_xml =
      '<?xml version="1.0" encoding="UTF-8" standalone="no"?>'
      . sprintf('<bitmaps count="%d">', scalar @types)
      . $bitmaps
      . '</bitmaps>';

    $s->write_to_file('PersonalPOI/Package/0/default/bitmaps.xml', $bitmaps_xml);

    my $categories_pc =
      '<?xml version="1.0" encoding="UTF-8" standalone="no"?>'
      . '<poicategories version="02010011">'
      . '<categories>' . $cats  . '</categories>'
      . '<types>'      . $types . '</types>'
      . '<search type="Generic">' . $srchs . '</search>'
      . '</poicategories>';

    $s->write_to_file('PersonalPOI/Package/0/default/categories.pc', $categories_pc);

    my $strings_en =
      '<?xml version="1.0" encoding="UTF-8" standalone="no"?>'
      . '<strings>' . $str_en . '</strings>';

    $s->write_to_file('PersonalPOI/Package/0/default/strings_en-GB.xml', $strings_en);

    my $strings_de =
      '<?xml version="1.0" encoding="UTF-8" standalone="no"?>'
      . '<strings>' . $str_de . '</strings>';

    $s->write_to_file('PersonalPOI/Package/0/default/strings_de-DE.xml', $strings_de);
  }
  
sub write_hash_file
  {
    my $s = shift;

    my $path = 'PersonalPOI/Package/0/default/';
    my $bs   = 524288;
        
    my @files = map { s/^$path// ? ($_) : () } @{$s->{files}};

    my $hashes = '';
    foreach my $f (sort @files)
      {
	my $pathname = $path . $f;

	my $file     = $s->filename($pathname);
	
	my @css = $s->blocked_sha256($file, $bs);

	$hashes
	  .= sprintf(qq{%s = "%s"\n}, 'FileName', $f)
	  .  sprintf(qq{%s = "%d"\n}, 'FileSize', -s $file)
	  .  sprintf(qq{%s = "%d"\n}, 'CheckSumSize', $bs);
	
	my $i = 0;
	foreach my $cs (@css)
	  {
	    my $index = '';
	    $index = $i if $i;
	    $i++;
	    $hashes .= sprintf(qq{%s%s = "%s"\n}, 'CheckSum', $index, $cs);
	  }
      }

    $s->write_to_file($path . 'hashes.txt', $hashes);
  }

